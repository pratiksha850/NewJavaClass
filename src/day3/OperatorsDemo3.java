package day3;

public class OperatorsDemo3 {
    public static void main(String[]args){
        //3.Increment/ Decrement Operator
        //++ --
        //Post  Pre
        //a++ a-- ++a --a

        int a = 5;
        System.out.println(a);
        a++;

        int x = 7;
        System.out.println(x);
        System.out.println(x++);
        System.out.println(x);

    }
}
