package day3;

public class LiteralsDemo {
    public static void main(String[]args){
        //literals: fixed value, datatype
        //1. Integer Literal 43435 1234 333
        byte b = 127;
        short s = 32767;
        int i = 2147483647;
        long l1 = 2147483647;
        long l2 = 2147483647L;
        long l3 = 9223372036854775807L;

        //2. Floating Literal 45.67 2323.56 (double)
        float f = 45.76F;
        double d = 45.6;

        //3. Boolean Literal true/false
        boolean b1 = true;
        boolean b2 = false;

        //4. Character Literal
        char c1 = 'a';
        char c2 = '1';
        char c3 = '#';
        char c4 = '\u0001';

        //5. String Literals "ram" ""
        String s1 = "max";
        String s2 = "xyz@123#T2";
        String s3 = "";
        String s4 = " ";
        String s5 = "123";
        String s6 = "12.34";
        String s7 = "true";
        String s8 = "Hello from java class.";
        String s9 = "2-16-2021";

        //6. Null Literals
        String s10 = null;

        //This is an object
        LiteralsDemo obj = new LiteralsDemo();
        LiteralsDemo ob = null;





    }
}
